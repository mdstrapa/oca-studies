package marcosoft.oca;

public class OperatorLab{

	public static void main(String[] args){


		int a = 6;

		System.out.println("The value of a++ is: " + a++); // it's suppoused to write 6

		System.out.println("The value of ++a is: " + ++a); // it's suppoused to write 8


		int b = -7;

		b = -b;

		System.out.println("The value of b is: " + b); // it's suppoused to write 7 positive


	}


}